    <script>
        document.getElementById('searchInput').addEventListener('input', function() {
            let filter = this.value.toLowerCase();
            let paketItems = document.querySelectorAll('.paket-item');

            paketItems.forEach(function(item) {
                let paketNameElement = item.querySelector('.bg-white');
                if (paketNameElement) {
                    let paketName = paketNameElement.textContent.toLowerCase().trim();
                    if (paketName.indexOf(filter) !== -1) {
                        item.style.display = 'block'; // Show the item
                    } else {
                        item.style.display = 'none'; // Hide the item
                    }
                }
            });
        });

                document.getElementById('tanggaltransaksi').addEventListener('click', function() {
            this.showPicker();
        });

        document.getElementById('bookingForm').addEventListener('submit', function(event) {
            event.preventDefault(); 

            var isValid = true;
            var elements = this.querySelectorAll('[required]');
            elements.forEach(function(element) {
                if (!element.value) {
                    isValid = false;
                }
            });

            if (isValid) {
                document.getElementById('successAlert').classList.remove('d-none');
            }
        });

        document.querySelector('.btn-close').addEventListener('click', function() {
            this.parentElement.classList.add('d-none');
        });
    </script>
